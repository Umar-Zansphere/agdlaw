export const blogPostSlugs = blogPosts.map((post) => post.slug);

export function getBlogPostBySlug(slug) {
  return blogPosts.find((post) => post.slug === slug) || null;
}

export function getRelatedBlogPosts(slug, count = 3) {
  const post = getBlogPostBySlug(slug);

  if (!post) return [];

  const explicit = (post.relatedSlugs || [])
    .map((relatedSlug) => getBlogPostBySlug(relatedSlug))
    .filter(Boolean);

  const seen = new Set([slug, ...explicit.map((item) => item.slug)]);
  const fallback = blogPosts.filter((item) => !seen.has(item.slug));

  return [...explicit, ...fallback].slice(0, count);
}
